float* FrequencyTable::computeFrequency( 
           int indexFreqsArray)
{
  // Check if position is a compress field (eq > 100)
  // If number is on the trailling value
  float freq;
  if (indexFreqsArray == -1)
  {
    freq = 0.5;
  }
  else
  {
    freq = freqsArray[indexFreqsArray] / 100.;
  }

  // If number is on 100 
  float* freqs = new float[ALPHABET_SIZE];
  if (freq > 1)
  {
    freqs[0] = 0.5;
  }
  else
  {
    freqs[0] = freq;
  }

  freqs[1] = 1-freqs[0];
  return freqs;
}

