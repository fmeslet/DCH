int FrequencyTable::getIndexFreqsArray( 
       int contextSize, int position, int key)
{

  // Init with de proba of preceding context size
  sumCountFreqsArray = \
    freqsCountContextArray[contextSize];
  
  // Compute sum until position-1
  for (int i=0; i<position; i++)
  {
    sumCountFreqsArray = sumCountFreqsArray + \
                          freqsCountArray[contextSize][i];
  }

  uint8_t countFreqsArray = freqsCountArray[ 
                        contextSize][position];
  int indexFreqsArray = this->getIndexFreqsArray( 
                        countFreqsArray, key);

  Serial.print("[DEBUG][FrequencyTable::getIndexFreqsArray] sumCountFreqsArray : ");
  Serial.println(sumCountFreqsArray);

  return indexFreqsArray;
}

int FrequencyTable::getIndexFreqsArray( 
       int countFreqsArray, int key)
{
  int indexFreqsArray = -1;
  int count = 0;
  int countTmp = 0;
  bool exitLoop = false;

  int i = 0;
  while((i < countFreqsArray) && (!exitLoop))
  {
    // Count number of value
    countTmp = (int)freqsArray[sumCountFreqsArray+i];
    // Compteur for equal freq value
    count = count + max(1, countTmp-100);

    // -1 because index start from 0
    if ((count-1) >= key)
    {
      indexFreqsArray = sumCountFreqsArray+i;
      exitLoop = true;
    }

    i = i + 1;
  }
  sumCountFreqsArray = sumCountFreqsArray+countFreqsArray;
  return indexFreqsArray;
}

